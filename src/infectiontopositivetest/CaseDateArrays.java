package infectiontopositivetest;

public class CaseDateArrays {
	String [] sordates;
	String [] sorcases;
	
	public CaseDateArrays(String[] sordates, String[] sorcases) {
		this.sorcases = sorcases;
		this.sordates = sordates;
	}
	
	public String[] getSorcases() {
		return sorcases;
	}
	public String[] getSordates() {
		return sordates;
	}
}
